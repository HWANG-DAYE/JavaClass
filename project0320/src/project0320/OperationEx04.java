package project0320;

public class OperationEx04 {
	public static void main(String[] args) {
		int n = 0;
		System.out.println(n);
		System.out.println(n+=3);
		System.out.println(n*=0);
		System.out.println(n+=5);
		System.out.println(n++);
		System.out.println(++n);
		System.out.println(n%5);
		System.out.println(--n);
		System.out.println(n--);
		System.out.println(n);
	}
}
