package unit_conversion_calculator;

public class UnitConvesion {
	public double Won_d(double dollar) {
		return dollar * 1469.91;
	}
	
	public double Won_y(double yen) {
		return yen * 9.9614;
	}
	
	public double Cm(double inch) {
		return inch * 2.54;
	}
	
	public double Kg(double pound) {
		return pound * 0.453592;
	}
	
	public double Meter(double pyeong) {
		return pyeong * 3.3;
	}
}
